class Contacts {
    public String fName;
    public String lName;
    public String number;

    public Contacts(String f, String l, String n) {
        this.fName = f;
        this.lName = l;
        this.number = n;
    }

    public String getfName() {
        return this.fName;
    }

    public String getlName() {
        return this.lName;
    }

    public String getNumber() {
        return this.number;
    }

}